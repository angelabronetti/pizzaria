    <p>História</p><br>
        Desde 1975 a pizzaria Labambina vem trazendo uma linha de produtos de alta qualidade, garantindo para o cliente oque a de melhor em pizzas, esfirras e muito mais. 
            Inaugurado pela familia Ventura de origem portuguesa, se iniciou no Brasil com o propósito de crescer e expandir o negócio, visando garantir conforto e comodidade para todos seus clientes.<br>
        Desde então nosso comércio tenta proporcionar o melhor ambiente da região, com uma vista explendida da cidade ao por do sol e a noite, fazendo do lugar confortavel e propicio para sua familia.
